import numpy as np
import os  # Модуль для работы с файлами и папками


# ГЕНЕРАЦИЯ ДАННЫХ

def generate_datasets(num_sets=70, min_size=100, max_size=10_000, data_dir='data'):
    """
    Функция для генерации файлов с наборами случайных чисел разного размера.
    Каждый файл содержит один набор чисел, по одному числу в строке.

    Параметры:
        num_sets: количество файлов (50–100)
        min_size: минимальный размер массива
        max_size: максимальный размер массива
        data_dir: папка для сохранения файлов
    """

    # Создаём папку для данных, если её ещё нет
    # Это важно, чтобы все файлы сохранялись в одной папке
    os.makedirs(data_dir, exist_ok=True)

    # # Генерация размеров наборов данных по логарифмической шкале
    # np.logspace генерирует числа от 10^log10(min_size) до 10^log10(max_size)
    # dtype=int — округляем до целых, чтобы размер массива был целым числом
    # Логарифмическая шкала используется, чтобы на маленьких размерах было больше точек, а на больших — меньше
    sizes = np.logspace(np.log10(min_size), np.log10(max_size), num=num_sets, dtype=int)

    # Убираем дубликаты, которые могут появиться после округления, и сортируем по возрастанию
    # Важно, чтобы каждый файл имел уникальный размер
    sizes = sorted(set(sizes))

    # Основной цикл по всем размерам наборов данных
    for i, size in enumerate(sizes, start=1):
        # Генерация случайных чисел от 1 до 1000 включительно
        # np.random.randint создаёт массив сразу нужного размера
        data = np.random.randint(1, 1001, size=size)

        # Формируем имя файла: data_номер.txt
        # Пример: data_1.txt
        filename = f"data_{i}.txt"

        # Создаём полный путь к файлу, объединяя папку и имя файла
        # Это важно, чтобы файл сохранился именно в нужной папке, а не в текущей рабочей директории
        filepath = os.path.join(data_dir, filename)

        # Сохраняем данные в текстовый файл
        # write() умеет работать только со строками, поэтому все числа нужно преобразовать в строки
        # '\n'.join(...) соединяет все числа в одну строку, разделяя переносами строк
        # Используем join вместо цикла for для записи всех чисел одним вызовом write(),
        # чтобы код работал быстрее на больших массивах
        with open(filepath, 'w') as file:
            file.write('\n'.join(map(str, data)) + '\n')  # записываем все числа сразу, с переносами строк

        # print(f"Создан {filename} (размер: {size})")


generate_datasets()


# СЧИТЫВАНИЕ ДАННЫХ

data_dir = "data"  # папка с файлами
num_files = len(os.listdir(data_dir)) # получаем кол-во файлов


# Функция для считывания одного набора чисел
def read_dataset(filepath):
    """
    Считывает набор чисел из текстового файла.

    Каждый элемент файла должен быть на отдельной строке.

    Параметры:
        filepath (str): путь к файлу с числами.

    Возвращает:
        list[int]: список чисел из файла.
    """
    numbers = []  # создаём пустой список для чисел
    with open(filepath, 'r') as file:  # открываем файл на чтение
        for line in file:  # проходим по каждой строке в файле
            # Каждая строка файла — это текст (строка).
            # Метод strip() убирает пробелы и символ переноса строки '\n'.
            # int(...) преобразует текст в число, чтобы с ним можно было выполнять математические операции.
            numbers.append(int(line.strip()))
    return numbers  # возвращаем список чисел


# Перебираем файлы по порядку
for i in range(1, num_files + 1):
    # Формируем имя файла (например: data_1.txt и тд)
    filename = f"data_{i}.txt"

    # Формируем полный путь к файлу
    filepath = os.path.join(data_dir, filename)

    # Считываем данные
    try:
        data = read_dataset(filepath)
    except FileNotFoundError:
        print('Файл не найден')
        continue

    # выводим имя файла, размер набора и первые 10 элементов для проверки
    print(f"{filename}: размер {len(data)}, первые 10 чисел: {data[:10]}")
