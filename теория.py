# ОЦЕНКА СЛОЖНОСТИ AVL-ДЕРЕВА
# СЛОЖНОСТЬ ДОБАВЛЕНИЯ
#
# Сложность операции добавления:
#
# O(log n)
#
# где n — количество элементов в дереве.
#
# AVL-дерево является сбалансированным бинарным деревом поиска.
# Для каждого узла разница высот левого и правого поддеревьев
# не превышает 1.
#
# Во время добавления алгоритм:
# 1. проходит от корня до нужного узла;
# 2. вставляет элемент;
# 3. при необходимости выполняет повороты.
#
# Высота AVL-дерева:
#
# h ≈ log n
#
# Поэтому количество посещаемых узлов также:
#
# O(log n)
#
# Повороты выполняются за:
#
# O(1)
#
# Следовательно:
#
# O(log n)

# СЛОЖНОСТЬ ПОИСКА
# Сложность поиска:
#
# O(log n)
#
# Поиск работает как в обычном бинарном дереве поиска.
#
# Если значение меньше текущего узла —
# переход выполняется в левое поддерево.
#
# Если больше —
# в правое.
#
# Так как дерево сбалансировано,
# его высота равна:
#
# O(log n)
#
# Следовательно количество сравнений
# также логарифмическое.

# СЛОЖНОСТЬ УДАЛЕНИЯ
# Сложность удаления:
#
# O(log n)
#
# Удаление включает:
# 1. поиск элемента;
# 2. удаление узла;
# 3. восстановление баланса дерева.
#
# Все действия происходят вдоль одной ветви дерева.
#
# Так как высота AVL-дерева:
#
# O(log n)
#
# то и удаление имеет такую же сложность.
#
# Повороты выполняются за:
#
# O(1)
#
# поэтому они не изменяют общую сложность.

# СРАВНЕНИЕ С РЕЗУЛЬТАТАМИ
# Во время выполнения программы измерялись:
# - время работы;
# - количество операций.
#
# Полученные результаты показали, что:
#
# - поиск выполняется быстрее всего;
# - вставка и удаление работают немного дольше;
# - количество операций растет медленно.
#
# При увеличении количества элементов
# время выполнения увеличивается незначительно,
# что соответствует сложности:
#
# O(log n)
#
# Экспериментальные результаты подтверждают
# теоретические оценки сложности AVL-дерева.

# ВЫВОД
# AVL-дерево обеспечивает эффективное выполнение:
# - добавления;
# - поиска;
# - удаления.
#
# Благодаря автоматической балансировке
# высота дерева остается логарифмической,
# поэтому операции выполняются быстро
# даже при большом количестве элементов.
#
# Практические результаты подтвердили
# теоретическую сложность:
#
# O(log n)